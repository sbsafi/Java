package loops;

public class NestedLoop_practice2 {

	public static void main(String[] args) {
		// Print this with nested loop

		// *****
		// *****
		// *****
		// *****

		for (int i = 0; i < 4; i++) { // This line iterates 4 lines/times
			for (int j = 0; j < 5; j++) { // This loop for 5 times/ 4 * eaches
				System.out.print("*");
			}
			System.out.println();
		}

	}

}
