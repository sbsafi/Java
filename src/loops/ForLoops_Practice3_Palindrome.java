package loops;

public class ForLoops_Practice3_Palindrome {

	public static void main(String[] args) {
		// create a loop that starts from 19 and stops at 3
		// decrease by one on every iteration
		// print the value in the same line

		for (int i = 19; i >= 3; i--) {
			System.out.print(i + ", ");

		}

	}

}
