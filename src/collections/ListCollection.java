package collections;

public class ListCollection {

	public static void main(String[] args) {
		// What is Array?
		// It is a container that can hold/store multiple values
		// Array is fixed in size
		// Fixed in Data type
		// Data maninuplation is hard with array ( delete, add, change or update).
		// Array is index order.
		// Array dont have any method.
		
		
		int [] numbers = new int [3]; // Here 3 is the length not the size
		numbers [0] = 10;
		numbers [1] = 30;
		numbers [2] = 9;
		
		// We dont have any method to remove or change the element but we can re-assign one.
		
		numbers [0] = 99;
		
		
		
	}

}
