package forEachLoop;

public class ForEach_1 {

	public static void main(String[] args) {
		// for each loop is designed to go through the entire collection

		int[] nums = { 1, 3, 5, 22, 6, 7 };

		for (int i : nums) {
			System.out.println(i);
			
			// line 10 ==> int is the data type we have stored in our list
			// i ==> is the pointer , we can name anything we want
			// nums ==> is the name of our list 
			
		}

	}

}
