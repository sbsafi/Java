package arrayList;

import java.util.ArrayList;

public class ArrayList_WrapperClass {

	public static void main(String[] args) {
		// What if we use the primitve data types?
		
		
		// Wrapper Classes
		// byte = Byte
		// short = Short
		// int = Integer
		// long = Long 
		// float = Float 
		// double = Double 
		// char = Char
		// boolean = Boolean
		
		ArrayList<Integer> age = new ArrayList<Integer>();
		
		age.add(12);
		age.add(44);
		age.add(20);
		
		for(int i = 0; i < age.size(); i++) {
			System.out.println(age.get(i));
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
